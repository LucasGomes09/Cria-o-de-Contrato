package com.mycompany.lucas.model;

public class Contrato {
    private String nome;
    private int id;
    private int id_cliente; // CORRIGIDO: de String para int
    private int id_empresa; // CORRIGIDO: de String para int
    private String descricao;
    private double valor;
    
    // CORRIGIDO: Construtor agora aceita int para os IDs
    public Contrato (int id, String nome, int id_cliente, int id_empresa, String descricao, double valor){
        this.id = id;
        this.nome = nome; 
        this.descricao = descricao;
        this.id_cliente = id_cliente;
        this.id_empresa = id_empresa;
        this.valor = valor;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the id_cliente
     */
    public int getId_cliente() {
        return id_cliente;
    }

    /**
     * @param id_cliente the id_cliente to set
     */
    public void setId_cliente(int id_cliente) {
        this.id_cliente = id_cliente;
    }

    /**
     * @return the id_empresa
     */
    public int getId_empresa() {
        return id_empresa;
    }

    /**
     * @param id_empresa the id_empresa to set
     */
    public void setId_empresa(int id_empresa) {
        this.id_empresa = id_empresa;
    }

    /**
     * @return the descricao
     */
    public String getDescricao() {
        return descricao;
    }

    /**
     * @param descricao the descricao to set
     */
    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    /**
     * @return the valor
     */
    public double getValor() {
        return valor;
    }

    /**
     * @param valor the valor to set
     */
    public void setValor(double valor) {
        this.valor = valor;
    }


}